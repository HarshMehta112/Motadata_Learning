public class TernaryOperator {

    public static void main(String[] args) {
        int x=45;
        int y=3;
        int max;

        System.out.println("First number is :"+x);
        System.out.println("Second number is :"+y);
        max=(x>y)?x:y;
        max=x+++y;
        System.out.println("Largest number is :"+max);


    }

}
