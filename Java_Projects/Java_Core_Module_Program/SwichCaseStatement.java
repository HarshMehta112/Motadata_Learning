public class SwichCaseStatement {

    public static void main(String[] args) {
        int day=187;
        switch (day){
            case 1:
                System.out.println("Monday");
                break;
            case 2:
                System.out.println("Turesday");
                break;
            case 3:
                System.out.println("Wendusday");
                break;
            case 4:
                System.out.println("Thursday");
                break;
            default:
                System.out.println("Enter the valid day");
                break;
        }

    }
}
